    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     *     int val;
     *     TreeNode left;
     *     TreeNode right;
     *     TreeNode() {}
     *     TreeNode(int val) { this.val = val; }
     *     TreeNode(int val, TreeNode left, TreeNode right) {
     *         this.val = val;
     *         this.left = left;
     *         this.right = right;
     *     }
     * }
     */
    static class Solution2 {
        List<Integer> ret = new ArrayList<>();
        int pre = -1;
        int max = 1;
        int count = 0;
        public int[] findMode(TreeNode root) {
            find1(root);

            int[] retArr = new int[ret.size()];
            for (int i = 0; i < ret.size(); i++) {
                retArr[i] = ret.get(i);
            }
            return retArr;
        }

        void find1(TreeNode root) {
            if (root == null) {
                return;
            }

            find1(root.left);
            if (root.val != pre && pre != -1) {
                count = 1;
                pre = root.val;
                if (count == max) {
                    ret.add(root.val);
                }
            }
            else if (pre == -1 || root.val == pre) {
                count ++;
                if (count == max) {
                    ret.add(root.val);
                }

                if (count > max) {
                    ret.clear();
                    ret.add(root.val);
                    max = count;
                }

                pre = root.val;
            }

            find1(root.right);
        }
    }
