    static class Solution {
        public int climbStairs(int n) {
            int pre1 = 0;
            int pre2 = 0;
            int sum = 0;
            for (int i = 1; i <= n; i++) {
                if (i == 1) {
                    pre1 = 1;
                    pre2 = 0;
                }
                if (i == 2) {
                    pre1 = 2;
                    pre2 = 0;
                }

                if (i == 3) {
                    pre1 = 2;
                    pre2 = 1;
                }

                sum = pre1 + pre2;
                pre2 = pre1;
                pre1 = sum;
            }
            return sum;
        }
    }
